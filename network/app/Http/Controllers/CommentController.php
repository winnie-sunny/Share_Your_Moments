     */
    public function store(Request $request)
    {
        $comment = new Comment();
        $request->validate([
            'content' => 'required|min:5|max:100' 
        ]);
        $comment->content = $request->content;
        $comment->user_id = Auth::id();
        $comment->post_id = $request->post_id;
        
        $comment->save();

        return response()->json($comment);
    }

